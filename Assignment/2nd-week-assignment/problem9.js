// 9. Describe the differences between the for loop, while loop, and do...while loop in JavaScript. When might you use each?

// for loop
for (let i = 0; i < 5; i++) {
    console.log(i);
  }
// while loop
let j = 0;
while (j < 5) {
  console.log(j);
  j++;
}
// do while loop

let x = 0;
do {
  console.log(x);
  x++;
} while (x < 5);
